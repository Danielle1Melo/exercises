val = int(input("Informe um valor de 1 a 7: "))
if val<1 or val>7 :
    print("Valor inválido")
else: 
    if val==1:
        print("Domingo")
    if val==2:
        print("Segunda")
    if val==3:
        print("Terça")
    if val==4:
        print("Quarta")
    if val==5:
        print("Quinta")
    if val==6:
        print("Sexta")
    if val==7:
        print("Sábado")